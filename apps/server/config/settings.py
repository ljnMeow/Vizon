def _require_bool_env(name: str) -> bool:
    """
    读取一个“必须显式配置”的布尔环境变量（fail-fast，不给默认值）。

    你提到“不希望 settings 里给默认值、以免掩盖错误”，所以这里采用强约束：
    - 变量不存在：直接抛错中断启动
    - 变量存在但值非法：直接抛错中断启动

    允许的值：
    - true:  1 / true / yes（大小写不敏感）
    - false: 0 / false / no
    """

    raw = os.getenv(name)
    if raw is None:
        raise RuntimeError(f"缺少必须的环境变量：{name}（请在 apps/server/.env 或系统环境变量中显式配置）")

    v = raw.strip().lower()
    if v in ("1", "true", "yes"):
        return True
    if v in ("0", "false", "no"):
        return False

    raise RuntimeError(f"环境变量 {name} 值非法：{raw!r}。允许 1/0/true/false/yes/no")



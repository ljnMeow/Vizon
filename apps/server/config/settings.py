def _require_positive_int_env(name: str) -> int:
    raw = os.getenv(name)
    if raw is None:
        raise RuntimeError(f"缺少必须的环境变量：{name}（请在 apps/server/.env 或系统环境变量中显式配置）")
    try:
        v = int(raw.strip())
    except Exception as ex:
        raise RuntimeError(f"环境变量 {name} 值非法：{raw!r}（必须是整数秒）") from ex
    if v <= 0:
        raise RuntimeError(f"环境变量 {name} 值非法：{raw!r}（必须是正整数秒）")
    return v


